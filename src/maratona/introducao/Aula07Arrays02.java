package maratona.introducao;
public class Aula07Arrays02 {
    public static void main(String[] args) {
        String[] nomes = new String[3];
        nomes[0] = "Zeca";
        nomes[1] = "Zica";
        nomes[2] = "Zico";
        for (int i = 0; i < 3; i++) {
            System.out.println(nomes[i]);
        }
    }
}
